class Seed:
    def __init__(self):
        self.__type = ""
        self.__name = ""
        self.__time = ""
        self.__earth = ""
        self.__water = ""
        self.__nutrients = ""
        self.__chemicalFertilizer = ""
        self.__clima = ""

        """
         Getter y Setter
        """
    def get_type(self):
        return self.__type

    def set_type(self, type):
        self.__type = type

    def get_name(self):
        return self.__name

    def set_name(self, name):
        self.__name = name

    def get_time(self):
        return self.__time

    def set_time(self, time):
        self.__time = time

    def get_earth(self):
        return self.__earth

    def set_earth(self, earth):
        self.__earth = earth

    def get_water(self):
        return self.__water

    def set_water(self, water):
        self.__water = water

    def get_nutrients(self):
        return self.__nutrients

    def set_nutrients(self, nutrients):
        self.__nutrients = nutrients

    def get_chemical_fertilizer(self):
        return self.__chemicalFertilizer

    def set_chemical_fertilizer(self, chemical_fertilizer):
        self.__chemicalFertilizer = chemical_fertilizer

    def get_clima(self):
        return self.__clima

    def set_clima(self, clima):
        self.__clima = clima


